#include <iostream>

using namespace std;

int main()
{

    //This is for addition
    int add_1 = 10;
    int add_2 = 30;
    int sum = add_1 + add_2 + 30 + 10;
    cout << "The sum is: " << sum << endl;

    //Subtraction
    int sub_1 = 10;
    int sub_2 = 20;
    int sub_3 = 40;
    short difference = sub_3 - sub_2 - sub_1;
    cout << "The difference is: " << difference << endl;

    // multiplication
    int nub_1 = 2;
    int nub_2 = 5;
    int multiplication = nub_1 * nub_2;
    cout << " multiply the tow numbers; "<< multiplication << endl;

    // now it for division
    double num_1 = 20;
    double num_2 = 10;
    double number = num_1 / num_2;
    cout << " the results are: "<< number << endl;


    return 0;
}
